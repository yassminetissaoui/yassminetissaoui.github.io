    <h5>
      7 janvier, 2014
    </h5>
    
    <blockquote cit="#">People say graphic design is so different now, 
      because you have so many more pixels and colors to work with... 
      But when you study art history, you see there's just nothing new under the sun. 
      Mosaics and needlework, it's all analogous to pixel and bitmap art. And with it all, 
      good design's not about what medium you're working in, it's about thinking hard about 
      what you want to do and what you have to work with before you start.
    </blockquote>
    
    <p> 
      Cette conférence de Susan Kare, le créatrice de nombreux éléments d'interface graphique pour le Macintosh d'apple, 
