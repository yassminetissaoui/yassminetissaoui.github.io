    <h3>
      LE DOUX, Adam.  
    </h3>

    <h4>
      <a href="https://make.bitsy.org/" target="_blank">
      Bitsy
      </a>
    </h4> 
    
    <h5>
      Moteur de jeu lancé par Adam De Loux, 2017
    </h5>
    
    <blockquote cit="#">little editor for little games or worlds
    </blockquote>
    
    <p> 
      Bitsy est un moteur de jeu web FOSS qui permet de créer de petites pièces en pixel-art que les joueurs peuvent explorer en 
      interagissant avec les personnes et les objets qui s'y trouvent. Son interface est composé de cinq fenêtres simples, dont seulement trois
      sont utilisées pour créer réellement un jeu. Depuis sa sortie, plus de 6000 jeux ont été réalisés à l'aide de cet outil et plusieurs plug-ins ont été crée pour lui.
    </p>
   

    <h6> 
     consulté le 26/09/2022
    </h6>
  
