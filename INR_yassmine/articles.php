      LE DOUX, Adam.  
    </h3>

    <h4>
      <a href="https://make.bitsy.org/" target="_blank">
      Bitsy,
      </a>
    </h4> 
    
    <h5>
      Moteur de jeu lancé par Adam De Loux, 2017.
    </h5>
    
    <blockquote cit="https://make.bitsy.org/">little editor for little games or worlds.
    </blockquote>
 
    <p class="readmore"> 
      Bitsy est un moteur de jeu web FOSS qui permet de créer de petites pièces en pixel-art que les joueurs peuvent explorer en interagissant avec les personnes et les objets qui s'y trouvent. Le moteur part du principe du WYSIWYG. Il est composé de plusieurs fenêtres qui permettent de dessiner les personnages, les objets, et le fond, de personnaliser la palette de couleurs qui est limité à trois couleurs pour chaque pièce, de créer de la musique chip-tune, et de manipuler le jeu directement par le code si c'est ce que l'utlisateur aimerait faire. 
<span id="dots"></span><span id="more">
 Depuis sa sortie, plus de 6000 jeux ont été réalisés à l'aide de cet outil et plusieurs plug-ins, hacks, et dérivés ont été créé.

Cet outil permet la création de jeux vidéos sous forme d'histoires interactives. Ces jeux ne sont pas contraints par la définition classique du jeu vidéo qui demande un système de régles à suivre, la compétition et la possibilité de gagner. Cet outil a surtout été utilisé pour raconter des histoires personnelles et courtes.</span></p>
