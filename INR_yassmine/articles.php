    <h3>
      LIALINA, Ola.  
    </h3>

    <h4>
      <a href="https://books.ub.uni-heidelberg.de/arthistoricum/catalog/book/972" target="_blank">
      Turing Complete User: Resisting Alienation in Human Computer Interaction,
      </a>
    </h4> 
    
    <h5>
    Heidelberg&#8201;: arthistoricum.net, 2021.
    </h5>
    
    <blockquote cit="https://books.ub.uni-heidelberg.de/arthistoricum/catalog/book/972">The role of “experience” is to hide the programmability or even customizability of the system,
      to minimize and channel users’ interaction with the system.
    </blockquote>
    
    <p> 
    Dans ce recueil d'essais, la net-artiste Ola Lialina 
    critique la conception contemporaine des interfaces utilisateur et explique l'évolution
    de la terminologie utilisée pour les décrire. Elle apporte un point de vue à la fois technique et philosophique et examine ce que signifie être un utilisateur à l'ére numérique.
<span id="dots2"></span><span id="more2">
Le terme «&#8201;utilisateur&#8201;» s'est vu attribuer une mauvaise réputation depuis l'apparition de l'ordinateur personnel. En citant Don Norman et Ted Nelson elle décrit comment la culture dominante de l'UX (User Experience) a fini par penser que les utilisateurs sont trop occupés pour avoir la pleine maîtrise des choses qu'ils créent, disent ou consomment ; se cachant derrière des mots humanistes tels que «&#8201;expérience&#8201;» au lieu d'interface, «&#8201;personnes&#8201;» au lieu d'utilisateurs et «&#8201;technologie&#8201;» au lieu d' «&#8201;ordinateur&#8201;», Lialina soutient que l'hégémonie vise à transformer les utilisateurs - quelqu'un qui porte des jugements, qui sait ce qu'il utilise et les effets de ce qu'il fait - en consommateurs aveugles. Elle cite Adobe comme principal exemple de commercialisation d'interfaces invisibles comme stimulateurs de créativité. 

L'internaute idéal de Lialina est celui qui est impliqué, engagé et conscient. Elle argumente que les utilisateurs devraient pouvoir «&#8201;atteindre leurs objectifs indépendamment de l'objectif principal d'une application ou d'un appareil&#8201;». Elle souhaite que les utilisateurs prennent constamment des décisions conscientes lorsqu'ils utilisent leur ordinateur, plutôt que d'être guidés par de grandes entreprises qui présument de ce qu’ils peuvent ou ne peuvent pas vouloir.
  </span></p>
