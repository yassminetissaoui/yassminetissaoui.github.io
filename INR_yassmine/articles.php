    </blockquote>
    
    <p> 
    Cet ouvrage est un manifeste pour démocratiser la création de jeux vidéos avec une barrière de compétences aussi proche que possible de zéro. Anthropy Anna, créatrice de jeux vidéos et d'éxpériences intéractives <span id="dots3"></span><span id="more3">
